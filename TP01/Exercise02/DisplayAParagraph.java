package Exercise02;

public class DisplayAParagraph {
    public static void main(String[] args) {
        System.out.printf("\\n            Line break.\n");
        System.out.printf("\\t            Tabulation.\n");
        System.out.printf("\\'          Single Quote.\n");
        System.out.printf("\\\"          Double Quote.\n");
        System.out.printf("\\\\                 \\Sign.\n");
        System.out.printf("\\\\\\               \\\\Sign.\n");
        System.out.printf("//          Line Comment.\n");
        System.out.printf("/*....*/   block Comment.\n");
        System.out.printf("\"\"\"\n            Text Comment.\n\"\"\"\n");
    }
}
